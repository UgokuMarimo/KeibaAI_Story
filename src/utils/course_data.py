# =========================================================
# 距離によるコース区分マッピング (Mapping Helper)
# =========================================================
# レース詳細（内・外）が不明な場合、距離から自動推定するための辞書
# ※ 例外もあるため、可能な限りスクレイピング時の「コース詳細」を優先すること

COURSE_TYPE_MAPPING = {
    '新潟': {
        1000: '直線',
        1200: '内', 1400: '内', 2000: '内', 2200: '内', 2400: '内',
        1600: '外', 1800: '外', 3000: '外', 3200: '外'
        # 1400は内外両方あるが、内がデフォルト
    },
    '中山': {
        1200: '外', 1600: '外', 2200: '外', 2600: '外', 3200: '外', 4000: '外',
        1800: '内', 2000: '内', 2500: '内', 3600: '内'
        # 1400は内外両方ある
    },
    '京都': {
        1100: '内', 1200: '内', 2000: '内',
        1400: '内', # 両方あるが内がメジャー? 外もありうる
        1600: '外', # デフォルト外 (マイルCSなど)
        1800: '外', 2200: '外', 2400: '外', 3000: '外', 3200: '外'
    },
    '阪神': {
        1200: '内', 2000: '内', 3000: '内',
        1400: '内', # 両方あるが...
        1600: '外', 1800: '外', 2400: '外', 2600: '外', 3200: '外'
    }
}

def get_course_features(place, surface, distance, detailed_course_str=None):
    """
    特徴量取得関数
    :param detailed_course_str: "芝2000m (外)" のような文字列がある場合、そこから判定
    """
    track_type = 'default'
    
    # 芝の場合のみ内・外の判定が必要
    if surface == '芝':
        # 1. 明示的なコース指定がある場合 (スクレイピングデータに「外」が含まれるか)
        if detailed_course_str and '外' in detailed_course_str:
            track_type = '外'
        elif detailed_course_str and '内' in detailed_course_str:
            track_type = '内'
        elif place == '新潟' and distance == 1000:
            track_type = '直線'
        else:
            # 2. 距離からの推定
            mapping = COURSE_TYPE_MAPPING.get(place, {})
            track_type = mapping.get(distance, 'default')

            # マッピングになく、かつ内外区分があるコースの場合はデフォルト(主に内)を設定
            if track_type == 'default' and place in ['新潟', '中山', '京都', '阪神']:
                # 京都・阪神の不明な1400/1600などは、暫定的に「外」の方がメジャーなケースが多いが、
                # 安全策として '内' または データ欠損を防ぐため '外' を割り当てるロジックが必要
                # ここでは辞書にあるキー(内/外)のどちらかに倒す処理が必要
                pass 

    # 辞書から取得 (キーがなければNoneまたは平均値を返す)
    features = COURSE_FEATURES.get((place, surface, track_type))
    
    # 該当なし（例: 京都芝1400で内外不明）の場合、'内'をフォールバックとして取得してみる
    if features is None and surface == '芝':
         features = COURSE_FEATURES.get((place, surface, '内'))
         
    return features
